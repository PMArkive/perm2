        void shiftchar( u16& p_val ) {
            if( '0' <= p_val && p_val <= '9' ) {
                p_val = p_val - '0';
                return;
            }
            if( p_val == '!' ) { // Lv.
                p_val = 10;
                return;
            }
            if( p_val == '/' ) {
                p_val = 11;
                return;
            }
            if( p_val == ' ' ) {
                p_val = 12;
                return;
            }
            if( 'A' <= p_val && p_val <= 'Z' ) {
                p_val = p_val - 'A' + 23;
                return;
            }
        }
